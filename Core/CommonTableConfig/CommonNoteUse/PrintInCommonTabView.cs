        /// <summary>
        /// 通过值的属性名来获取列名
        /// </summary>
        /// <param name="ValueName"></param>
        /// <returns></returns>
        public string GetCaptionName(string ValueName)
        {
            try
            {
                string namestr = "";
                string numstr = ValueName.Substring(ValueName.IndexOf("Value")+5);
                string captoinName = "Name" + numstr;
                PropertyInfo propertyInfo = this.GetType().GetProperty(captoinName);
                object objstr = propertyInfo.GetValue(this, null);
                if (objstr != null)
                {
                    namestr= objstr.ToString();
                }
                return namestr;
            }
            catch (Exception ex)
            {
                throw new Exception("属性名不存在");
            }
        }

